# tipos de datos en python.

#1) string (str) = Cadena de texto "" o '', se usa para escribir texto simple.

mi_nombre = "Leonardo"
print(mi_nombre)
print("Esto es una cadena de texto")

# 2) Number integer = int = Numero entero.

print(2)
print(4)

# 3) Numeros decimales (float)

print(6.2)

# 4) Boolean = booleano, identifica si es verdadero o false.

print(True)
print(False)

# RETO

entero_soy = 6
print(entero_soy)

decimo_soy = 5.7
print(decimo_soy)

hola = "Hola mundo que tal"
print(hola)

Uno_miente = (True)

No_es_verdad = (False)

print(Uno_miente)
print(No_es_verdad)

# Metodo para saber que tipo de dato es unavariable.(type)

print(type(entero_soy))
print(type(decimo_soy))
print(type(hola))
print(type(Uno_miente))
print(type(No_es_verdad))

